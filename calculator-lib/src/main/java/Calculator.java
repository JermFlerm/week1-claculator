public class Calculator {
	/**
	 * Multiplies <i><strong>three</strong></i> numbers.
	 * @param num 1 First number
	 * @param num 2 Second number
	 * @param num 3 Third number
	 * @return Product of three numbers
	 **/
	public float multiply(float num1, float num2, float num3) {
		return num1*num2*num3;
	}
}
